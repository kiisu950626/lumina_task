def query_authorized_elders(user_ref: str) -> list[dict]:
    """Return all elders the user has permission to view."""
    sql = """
        SELECT 
            e.id AS elder_id,
            e.name AS elder_name,
            e.birth_date,
            e.emergency_phone,
            cg.id AS group_id,
            cg.group_name,
            gm.role_in_group
        FROM group_members gm
        JOIN care_groups cg ON gm.group_id = cg.id
        JOIN elders e ON cg.elder_id = e.id
        WHERE gm.user_id = %s 
          AND e.deleted_at IS NULL;
    """
    with get_db_connection() as conn:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            user_pk = _resolve_user_pk(cur, user_ref)
            if not user_pk:
                return []
                
            cur.execute(sql, (user_pk,))
            return [dict(row) for row in cur.fetchall()]

# ── EMERGENCY PUSH SYSTEM ────────────────────────────────────────────────────

def query_emergency_push_tokens(elder_id: str, exclude_user_id: Optional[str] = None) -> list[dict]:
    """Retrieve FCM tokens for all active devices belonging to the elder's care group."""
    sql = """
        SELECT DISTINCT ud.fcm_token, ud.device_type, u.full_name, u.id AS user_id
        FROM elders e
        JOIN care_groups cg ON e.id = cg.elder_id
        JOIN group_members gm ON cg.id = gm.group_id
        JOIN users u ON gm.user_id = u.id
        JOIN user_devices ud ON u.id = ud.user_id
        WHERE e.id = %s 
          AND u.deleted_at IS NULL 
          AND e.deleted_at IS NULL
    """
    params = [elder_id]
    if exclude_user_id:
        sql += " AND u.id != %s"
        params.append(exclude_user_id)

    with get_db_connection() as conn:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            elder_pk = _resolve_elder_pk(cur, elder_id)
            if not elder_pk:
                return []
                
            cur.execute(sql, tuple(params))
            return [dict(row) for row in cur.fetchall()]

