"""
CuraGo — PostgreSQL / Relational Database Layer
=====================================================

This module is aligned with the current UUID / SERIAL PostgreSQL schema and
provides enterprise-grade Data Access Objects (DAO) for the backend API.

Schema assumptions handled here:
  - PostgreSQL generates UUID / BIGSERIAL primary keys.
  - Soft deletion is enforced: `deleted_at IS NULL` must be appended to core queries.
  - Health measurements enforce strict BP logic: `systolic_bp > diastolic_bp`.
  - HNSW indexing and `vector_cosine_ops` are used for AI embedding similarity search.

Important implementation details:
  - All SQL uses parameterized inputs to prevent SQL injection.
  - Read queries use autocommit connections for performance.
  - Write operations (execute_*) use explicit `conn.autocommit = False`, bundled inside 
    `try...except...conn.rollback()` blocks to ensure ACID compliance.
  - Unknown lookups return `None` or `(False, message)` rather than raising application errors.
"""

import hashlib
import secrets
from datetime import datetime, timezone
from typing import Any, Optional
from uuid import UUID
from contextlib import contextmanager

import psycopg2
import psycopg2.extras
from psycopg2.pool import ThreadedConnectionPool
from pgvector.psycopg2 import register_vector
from psycopg2.extras import RealDictCursor

from skeleton.config import DB_CONFIG

# ── Database Connection Pool Setup ───────────────────────────────────────────

# 利用 config.py 的字典動態組裝連線字串
PG_DSN = f"dbname={DB_CONFIG['dbname']} user={DB_CONFIG['user']} password={DB_CONFIG['password']} host={DB_CONFIG['host']} port={DB_CONFIG['port']}"

# 初始化連線池 (設定最少 1 個，最多 20 個連線)
db_pool = ThreadedConnectionPool(1, 20, PG_DSN)

@contextmanager
def get_db_connection(autocommit: bool = True):
    """
    從 ThreadedConnectionPool 取得連線，並確保使用完畢後自動歸還。
    預設為 autocommit=True (適合純讀取)。寫入操作請傳入 autocommit=False。
    """
    conn = db_pool.getconn()
    conn.autocommit = autocommit
    try:
        register_vector(conn)
        yield conn
    finally:
        db_pool.putconn(conn)

# ── connection / id / security helpers ───────────────────────────────────────

def _is_uuid(value: Any) -> bool:
    try:
        UUID(str(value))
        return True
    except Exception:
        return False

def _hash_password(value: str | None) -> str | None:
    """Hash a password using Argon2id when available, or fallback to PBKDF2."""
    if value is None:
        return None

    try:
        from argon2 import PasswordHasher  # type: ignore
        return PasswordHasher().hash(str(value))
    except ImportError:
        iterations = 100_000
        salt = secrets.token_hex(16)
        hashed = hashlib.pbkdf2_hmac(
            "sha256",
            str(value).encode("utf-8"),
            salt.encode("utf-8"),
            iterations,
        ).hex()
        return f"$argon2id$pbkdf2_fallback$v=19$i={iterations}${salt}${hashed}"

def _verify_password(value: str, stored_hash: str | None) -> bool:
    """Verify password_hash generated by _hash_password()."""
    if not value or not stored_hash:
        return False

    if stored_hash.startswith("$argon2id$pbkdf2_fallback$"):
        try:
            parts = stored_hash.split("$")
            iterations = int(parts[4].split("=", 1)[1])
            salt = parts[5]
            expected_hash = parts[6]
            actual_hash = hashlib.pbkdf2_hmac(
                "sha256",
                str(value).encode("utf-8"),
                salt.encode("utf-8"),
                iterations,
            ).hex()
            return secrets.compare_digest(actual_hash, expected_hash)
        except Exception:
            return False

    if stored_hash.startswith("$argon2id$"):
        try:
            from argon2 import PasswordHasher  # type: ignore
            from argon2.exceptions import VerifyMismatchError  # type: ignore
            try:
                return PasswordHasher().verify(stored_hash, str(value))
            except VerifyMismatchError:
                return False
        except Exception:
            return False

    return False

def _resolve_user_pk(cur, user_ref: Any) -> Optional[str]:
    """Resolve users.id from UUID or email, enforcing soft-delete check."""
    if user_ref is None:
        return None

    ref = str(user_ref)
    if _is_uuid(ref):
        cur.execute("SELECT id FROM users WHERE id = %s::uuid AND deleted_at IS NULL;", (ref,))
    elif "@" in ref:
        cur.execute("SELECT id FROM users WHERE email = %s AND deleted_at IS NULL;", (ref,))
    else:
        return None

    row = cur.fetchone()
    return str(row["id"]) if row else None

def _resolve_elder_pk(cur, elder_ref: Any) -> Optional[str]:
    """Resolve elders.id from UUID, enforcing soft-delete check."""
    if not elder_ref or not _is_uuid(elder_ref):
        return None

    cur.execute("SELECT id FROM elders WHERE id = %s::uuid AND deleted_at IS NULL;", (str(elder_ref),))
    row = cur.fetchone()
    return str(row["id"]) if row else None

# ── AUTHENTICATION QUERIES ───────────────────────────────────────────────────

def register_user(email: str, phone: str, full_name: str, password: str, role: str) -> tuple[bool, str]:
    """Register a new user. Returns (True, user_id) or (False, error_message)."""
    with get_db_connection(autocommit=False) as conn:
        try:
            with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
                # 1. 檢查信箱是否已存在
                cur.execute("SELECT 1 FROM users WHERE email = %s;", (email,))
                if cur.fetchone():
                    conn.rollback()
                    return False, "Email is already registered."

                # 2. 驗證角色合法性
                if role not in ('family', 'caregiver', 'admin'):
                    conn.rollback()
                    return False, "Invalid role specified."

                password_hash = _hash_password(password)
                if password_hash is None:
                    conn.rollback()
                    return False, "Password cannot be empty."

                # 3. 執行寫入
                cur.execute(
                    """
                    INSERT INTO users (email, phone, password_hash, full_name, role)
                    VALUES (%s, %s, %s, %s, %s)
                    RETURNING id;
                    """,
                    (email, phone, password_hash, full_name, role)
                )
                
                new_user = cur.fetchone()
                conn.commit()
                return True, str(new_user["id"])

        except Exception as e:
            conn.rollback()
            return False, str(e)

def login_user(email: str, password: str) -> Optional[dict]:
    """Verify password hash and return a user dict on success, else None."""
    sql_select = """
        SELECT
            id AS user_id,
            email,
            phone,
            full_name,
            role,
            password_hash
        FROM users
        WHERE email = %s
          AND deleted_at IS NULL;
    """
    sql_update = "UPDATE users SET last_login_at = NOW() WHERE id = %s"

    with get_db_connection(autocommit=False) as conn:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            # 1. 查詢使用者
            cur.execute(sql_select, (email,))
            row = cur.fetchone()

            # 2. 驗證失敗則 rollback 並提早結束
            if not row or not _verify_password(password, row["password_hash"]):
                conn.rollback()
                return None

            # 3. 驗證成功，使用同一個 cursor 直接更新最後登入時間
            try:
                cur.execute(sql_update, (row["user_id"],))
                conn.commit()
            except Exception:
                conn.rollback()

            return {
                "user_id": str(row["user_id"]),
                "email": row["email"],
                "phone": row["phone"],
                "full_name": row["full_name"],
                "role": row["role"]
            }

