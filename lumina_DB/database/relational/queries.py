def query_daily_pending_tasks(elder_id: str) -> list[dict]:
    """Retrieve today's pending tasks for a specific elder."""
    sql = """
        SELECT 
            t.id AS task_id,
            t.task_type,
            t.scheduled_time,
            u.full_name AS assigned_caregiver_name
        FROM tasks t
        LEFT JOIN users u ON t.assigned_to = u.id
        WHERE t.elder_id = %s
          AND t.status = 'pending'
          AND DATE(t.scheduled_time AT TIME ZONE 'UTC') = CURRENT_DATE
          AND t.deleted_at IS NULL
        ORDER BY t.scheduled_time ASC;
    """
    with get_db_connection() as conn:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            elder_pk = _resolve_elder_pk(cur, elder_id)
            if not elder_pk:
                return []
                
            cur.execute(sql, (elder_pk,))
            return [dict(row) for row in cur.fetchall()]

def execute_complete_task(task_id: str) -> tuple[bool, str | dict]:
    """Atomically mark a task as completed."""
    if not _is_uuid(task_id):
        return False, "Invalid task ID format."

    with get_db_connection(autocommit=False) as conn:
        try:
            with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
                cur.execute(
                    """
                    UPDATE tasks 
                    SET status = 'completed', 
                        completed_at = NOW() 
                    WHERE id = %s 
                      AND status = 'pending'
                      AND deleted_at IS NULL
                    RETURNING id, elder_id, task_type, completed_at;
                    """,
                    (str(task_id),)
                )
                updated_task = cur.fetchone()
                
                if not updated_task:
                    conn.rollback()
                    return False, "Task not found, already completed, or deleted."

                conn.commit()
                return True, dict(updated_task)

        except Exception as e:
            conn.rollback()
            return False, str(e)

# ── HEALTH & AI MEASUREMENTS ─────────────────────────────────────────────────

def execute_record_health_measurement(
    elder_id: str,
    measured_at: str,
    data_source: str = 'manual',
    heart_rate: Optional[int] = None,
    systolic_bp: Optional[int] = None,
    diastolic_bp: Optional[int] = None,
    blood_sugar: Optional[float] = None,
    meal_context: Optional[str] = None,
    ai_evaluation: Optional[str] = None,
    ai_reasoning: Optional[str] = None,
    ai_suggestion: Optional[str] = None,
) -> tuple[bool, str | dict]:
    """
    Insert a health measurement. Relies on DB constraints (e.g., systolic > diastolic)
    to enforce medical data integrity.
    """
    with get_db_connection(autocommit=False) as conn:
        try:
            with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
                elder_pk = _resolve_elder_pk(cur, elder_id)
                if not elder_pk:
                    conn.rollback()
                    return False, "Elder not found or deleted."

                cur.execute(
                    """
                    INSERT INTO health_measurements (
                        elder_id, heart_rate, systolic_bp, diastolic_bp, blood_sugar, 
                        meal_context, data_source, ai_evaluation, ai_reasoning, 
                        ai_suggestion, measured_at
                    ) VALUES (
                        %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s
                    ) RETURNING id, measured_at, created_at;
                    """,
                    (
                        elder_pk, heart_rate, systolic_bp, diastolic_bp, blood_sugar,
                        meal_context, data_source, ai_evaluation, ai_reasoning,
                        ai_suggestion, measured_at
                    )
                )
                record = cur.fetchone()
                conn.commit()
                return True, dict(record)

        except psycopg2.errors.CheckViolation as e:
            conn.rollback()
            return False, f"Data validation failed (Check Constraint Violation): {e}"
        except Exception as e:
            conn.rollback()
            return False, str(e)

# ── AI VECTOR & EVENT SEARCH ─────────────────────────────────────────────────

def query_similar_historical_events(elder_id: str, embedding_vector: list[float], limit: int = 3) -> list[dict]:
    """Find the most semantically similar historical events using pgvector Cosine Distance."""
    if len(embedding_vector) != 768:
        raise ValueError("Embedding vector must be exactly 768 dimensions.")

    sql = """
        SELECT 
            id AS event_id,
            original_text,
            severity,
            status,
            created_at,
            (embedding <=> %s::vector) AS cosine_distance
        FROM events
        WHERE elder_id = %s
          AND deleted_at IS NULL
          AND embedding IS NOT NULL
        ORDER BY embedding <=> %s::vector
        LIMIT %s;
    """
    
    with get_db_connection() as conn:
        with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
            elder_pk = _resolve_elder_pk(cur, elder_id)
            if not elder_pk:
                return []
                
            cur.execute(sql, (embedding_vector, elder_pk, embedding_vector, limit))
            return [dict(row) for row in cur.fetchall()]

