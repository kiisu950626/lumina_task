# ── EVENT OPERATIONS (AI & 家屬端) ──────────────────────────────────────────

def execute_create_event(
    elder_id: str,
    reporter_id: str,
    source_language: str,
    original_text: str,
    event_type: str,
    severity: str,
    normalized_text: Optional[str] = None,
    translations: Optional[dict] = None,
    embedding_vector: Optional[list[float]] = None
) -> tuple[bool, str | dict]:
    """
    接收 AI 層辨識完的結果，建立一筆新的異常事件。
    包含多國語言翻譯 (JSONB) 與語意向量 (Vector)。
    """
    with get_db_connection(autocommit=False) as conn:
        try:
            with conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
                elder_pk = _resolve_elder_pk(cur, elder_id)
                reporter_pk = _resolve_user_pk(cur, reporter_id)
                
                if not elder_pk or not reporter_pk:
                    conn.rollback()
                    return False, "Elder or Reporter not found."

                cur.execute(
                    """
                    INSERT INTO events (
                        elder_id, reporter_id, source_language, original_text, 
                        normalized_text, translations, embedding, 
                        event_type, severity, status
                    ) VALUES (
                        %s, %s, %s, %s, %s, %s, %s::vector, %s, %s, 'pending'
                    ) RETURNING id, created_at;
                    """,
                    (
                        elder_pk, reporter_pk, source_language, original_text,
                        normalized_text, psycopg2.extras.Json(translations or {}), 
                        embedding_vector,
                        event_type, severity
                    )
                )
                new_event = cur.fetchone()
                conn.commit()
                return True, dict(new_event)

        except Exception as e:
            conn.rollback()
            return False, str(e)

